
public class LoopFor {
	
	public void loopFor1() {
		
		for(int contador = 0; contador <=10; contador++) {
			
			System.out.println("VALOR DO CONTADOR DO LOOP FOR: " + contador);
		}
	}
	
	public void loopFor2(int limite) {
		
		for(int contador = 0; contador <=limite; contador++) {
			
			System.out.println("VALOR DO CONTADOR DO LOOP FOR: " + contador);
		}
}

}
